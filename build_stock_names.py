"""Build stock name mapping from akshare"""
import sys, os, json
sys.path.insert(0, r"D:\quant_a_stock")
from config import DATA_DIR, OUTPUT_DIR

def build_names():
    """Fetch all A-stock names from akshare"""
    try:
        import akshare as ak
        print("[Names] Fetching from akshare...")
        df = ak.stock_zh_a_spot_em()
        name_map = {}
        for _, row in df.iterrows():
            code = str(row.get("代码", "")).strip()
            name = str(row.get("名称", "")).strip()
            if code and name and name != "nan":
                name_map[code] = name
        print(f"[Names] Got {len(name_map)} names from akshare")
    except Exception as e:
        print(f"[Names] akshare failed: {e}, trying baostock...")
        name_map = _from_baostock()
    
    # Also try to enrich from industry data cache
    _enrich_from_industry(name_map)
    
    out = os.path.join(DATA_DIR, "stock_names.json")
    with open(out, "w", encoding="utf-8") as f:
        json.dump(name_map, f, ensure_ascii=False)
    print(f"[Names] Saved {len(name_map)} names to {out}")
    return name_map

def _from_baostock():
    """Fallback: get names from baostock cache"""
    name_map = {}
    try:
        import pickle
        import baostock as bs
        path = os.path.join(DATA_DIR, "stock_list_all.pkl")
        if os.path.exists(path):
            with open(path, "rb") as f:
                codes = pickle.load(f)
            # Try querying stock basic
            bs.login()
            for code in codes[:100]:
                try:
                    rs = bs.query_stock_basic(code)
                    if rs.error_code == "0":
                        while rs.next():
                            row = rs.get_row_data()
                            if len(row) > 1:
                                plain = code.split(".")[-1] if "." in code else code
                                name_map[plain] = row[1]
                except:
                    pass
            bs.logout()
            print(f"[Names] Got {len(name_map)} from baostock")
    except Exception as e:
        print(f"[Names] baostock fallback failed: {e}")
    
    if not name_map:
        # Hardcoded HS300 fallback
        name_map = HS300_NAMES
        print(f"[Names] Using hardcoded HS300 names: {len(name_map)}")
    
    return name_map

def _enrich_from_industry(name_map):
    """Try to get more names from industry data"""
    import pickle
    path = os.path.join(DATA_DIR, "industry_bs.pkl")
    if os.path.exists(path):
        try:
            df = pickle.load(open(path, "rb"))
            for _, row in df.iterrows():
                code = str(row.get("code", "")).strip()
                name = str(row.get("code_name", "")).strip()
                if "." in code:
                    code = code.split(".")[-1]
                if code and name and name != "nan" and code not in name_map:
                    name_map[code] = name
            print(f"[Names] Enriched: {len(name_map)} total")
        except:
            pass

